GAP_PENALTY = 9
import copy

class Cell:
    def __init__(self, pos_x, pos_y):
        self.score = -1
        self.pos_x = pos_x
        self.pos_y = pos_y
        self.seq1 = 'L'     # means lambda
        self.seq2 = 'L'     # means lambda
        self.parents = []

    def set_score(self, score):
        self.score = score

    def set_seq1(self, seq1_element):
        self.seq1 = seq1_element

    def set_seq2(self, seq2_element):
        self.seq2 = seq2_element

    def set_parent(self, parent_cell):
        self.parents = parent_cell

    def __str__(self):
        return "|" + str(self.score) + "|"


def initialize_matrix(seq1, seq2):
    matrix = []
    seq1 = 'L' + seq1
    seq2 = 'L' + seq2
    for x in range(len(seq2)):
        tmp = []
        for y in range(len(seq1)):
            cell = Cell(x, y)
            cell.set_seq1(seq1[y])
            cell.set_seq2(seq2[x])
            if x == 0 or y == 0:
                cell.set_score(0)
            tmp.append(cell)
        matrix.append(tmp)
    return matrix


def calculate_score(matrix, x, y):
    # returns score of that cell and an array of its parent cells
    val1_match_or_mismatch = PAM250[matrix[x][y].seq1][matrix[x][y].seq2] + matrix[x-1][y-1].score
    val2_gap_seq1 = matrix[x-1][y].score - GAP_PENALTY
    val3_gap_seq2 = matrix[x][y-1].score - GAP_PENALTY

    max_value = max([val1_match_or_mismatch, val2_gap_seq1, val3_gap_seq2])

    # find indexes for parents
    parents = []
    if max_value == val1_match_or_mismatch:
        parents.append(matrix[x-1][y-1])
    if max_value == val2_gap_seq1:
        parents.append(matrix[x-1][y])
    if max_value == val3_gap_seq2:
        parents.append(matrix[x][y-1])
    return max_value, parents


def calculate_matrix_scores(matrix):
    for x in range(1, len(matrix)):
        for y in range(1, len(matrix[1])):
            score, parents = calculate_score(matrix, x, y)
            matrix[x][y].set_score(score)
            matrix[x][y].set_parent(parents)
    return matrix


def traceback(path, cell, paths):
    position = (cell.pos_x, cell.pos_y)
    if cell.score == 0:
        path.append(position)
        paths.append(path)
        return paths
    else:
        path.append(position)
        parents = cell.parents
        for p in parents:
            _ = traceback(copy.deepcopy(path), p, paths)
        return _


def find_path(matrix):
    max_score = 0
    for x in range(len(matrix)):
        if matrix[x][-1].score >= max_score:
            max_score = matrix[x][-1].score

    for y in range(len(matrix[1])):
        if matrix[-1][y].score >= max_score:
            max_score = matrix[-1][y].score

    # find other max positions. There may be more than one maximum
    max_score_positions = []
    for x in range(len(matrix)):
        if matrix[x][-1].score == max_score:
            max_score_positions.append((x, len(matrix[1])-1))

    for y in range(len(matrix[0])):
        if matrix[-1][y].score == max_score:
            max_score_positions.append((len(matrix)-1, y))

    paths = []
    for msp in max_score_positions:
        tmp_cell = matrix[msp[0]][msp[1]]
        paths_from_traceback = traceback([], tmp_cell, [])
        paths = paths + paths_from_traceback

    return paths, max_score


def find_answer(matrix, path):
    # before these i should generate different paths
    sequence1 = ""
    sequence2 = ""
    for p_i in range(len(path)):
        try:
            cell = matrix[path[p_i][0]][path[p_i][1]]
            if path[p_i][1] > path[p_i+1][1]:
                sequence1 = cell.seq1 + sequence1
            elif path[p_i][1] == path[p_i+1][1]:
                sequence1 = "-" + sequence1

            if path[p_i][0] > path[p_i+1][0]:
                sequence2 = cell.seq2 + sequence2
            elif path[p_i][0] == path[p_i+1][0]:
                sequence2 = "-" + sequence2
        except:
            pass

    # here rest of the sequence from its head will be added to it
    if path[p_i][0] < path[p_i][1]:
        x = path[p_i][0]
        y = path[p_i][1]
        sub = y - x
        while y != 0:
            sequence1 = matrix[x][y].seq1 + sequence1
            y = y - 1

        y = path[p_i][1]
        while x != 0:
            sequence2 = matrix[x][y].seq2 + sequence2
            x = x - 1

        for i in range(sub): sequence2 = '-' + sequence2

    if path[p_i][0] > path[p_i][1]:
        x = path[p_i][0]
        y = path[p_i][1]
        sub = x - y

        while x != 0:
            sequence2 = matrix[x][y].seq2 + sequence2
            x = x - 1

        x = path[p_i][0]
        while y != 0:
            sequence1 = matrix[x][y].seq1 + sequence1
            y = y - 1

        for i in range(sub): sequence1 = '-' + sequence1

    # here rest of the sequence from its tail will be added to it
    if path[0][0] < len(matrix) - 1:
        x = path[0][0]
        y = path[0][1]
        while x != len(matrix) - 1:
            sequence2 = sequence2 + matrix[x+1][y].seq2
            x = x + 1

    if path[0][1] < len(matrix[0]) - 1:
        x = path[0][0]
        y = path[0][1]
        while y != len(matrix[0]) - 1:
            sequence1 = sequence1 + matrix[x][y+1].seq1
            y = y + 1

    if len(sequence2) > len(sequence1):
        sub_len = len(sequence2) - len(sequence1)
        for i in range(sub_len): sequence1 = sequence1 + '-'

    if len(sequence2) < len(sequence1):
        sub_len = len(sequence1) - len(sequence2)
        for i in range(sub_len): sequence2 = sequence2 + '-'

    return sequence1, sequence2



